CREATE PROCEDURE `FillRecommendData`()
BEGIN
    DECLARE i INT DEFAULT 1;
    DECLARE rand_cat VARCHAR(20);
    DECLARE rand_target VARCHAR(100);
    DECLARE rand_pos VARCHAR(50);
    DECLARE rand_region VARCHAR(10);
    
    WHILE i <= 200 DO
        -- 随机分配类别
        SET rand_cat = ELT(FLOOR(1 + RAND() * 5), 'education', 'elderly', 'environment', 'charity', 'culture');
        
        -- 根据类别精准匹配服务对象（增加相关性）
        SET rand_target = CASE rand_cat
            WHEN 'education' THEN ELT(FLOOR(1+RAND()*3), '小学生', '留守儿童', '贫困生')
            WHEN 'elderly' THEN ELT(FLOOR(1+RAND()*3), '独居老人', '敬老院老人', '空巢老人')
            WHEN 'environment' THEN ELT(FLOOR(1+RAND()*2), '社区居民', '广大市民')
            WHEN 'charity' THEN ELT(FLOOR(1+RAND()*2), '残障人士', '流浪小动物')
            ELSE '体育爱好者' END;

        -- 随机属地 (涵盖北京1101, 上海3101, 苏州3205, 南京3201)
        SET rand_region = ELT(FLOOR(1 + RAND() * 4), '110101', '310115', '320508', '320102');

        -- 插入活动
        INSERT INTO `activity` (org_id, project_code, title, category_id, region_code, target_audience, start_time, end_time, status, description)
        VALUES (
            1, 
            CONCAT('REC', LPAD(i, 7, '0')), 
            CONCAT(rand_target, rand_cat, '专项项目-', i),
            rand_cat, 
            rand_region, 
            rand_target, 
            '2026-05-01 09:00:00', '2026-05-10 09:00:00', 0,
            CONCAT('这是为推荐算法生成的测试数据，重点针对', rand_target, '开展服务。')
        );

        -- 为每个活动插入 2 个特定岗位
        SET rand_pos = CASE rand_cat
            WHEN 'education' THEN '支教老师'
            WHEN 'elderly' THEN '陪护志愿者'
            WHEN 'environment' THEN '环保宣传员'
            ELSE '通用服务岗' END;

        INSERT INTO `activity_position` (activity_id, pos_name, plan_count) 
        VALUES (LAST_INSERT_ID(), rand_pos, 10), (LAST_INSERT_ID(), '领队', 2);

        SET i = i + 1;
    END WHILE;
END
